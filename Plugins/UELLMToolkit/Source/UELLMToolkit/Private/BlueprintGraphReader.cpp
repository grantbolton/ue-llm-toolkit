FString FBlueprintGraphReader::GetBlendPinLabel(UEdGraphPin* Pin, UEdGraphNode* OwnerNode)
{
	if (!Pin) return FString();

	// Use display name if available
	FText DisplayName = Pin->GetDisplayName();
	if (!DisplayName.IsEmpty())
	{
		return DisplayName.ToString();
	}

	// For blend-by-enum nodes, try to map the index to enum entry
	FString PinName = Pin->PinName.ToString();
	if (PinName.StartsWith(TEXT("BlendPose_")))
	{
		// Extract index
		FString IndexStr = PinName.RightChop(10); // "BlendPose_" is 10 chars
		int32 Index = FCString::Atoi(*IndexStr);
		if (Index == 0)
		{
			return TEXT("Default");
		}
	}

	if (PinName == TEXT("Source") || PinName == TEXT("Result") || PinName == TEXT("Pose"))
	{
		return PinName;
	}

	return PinName;
}

void FBlueprintGraphReader::WalkPoseChain(UEdGraphNode* Node, int32 Depth, FString& Output,
	TSet<UEdGraphNode*>& Visited, const FString& PinLabel)
{
	if (!Node) return;

	if (Visited.Contains(Node))
	{
		FString Prefix = FString::ChrN(Depth * 2, TEXT(' '));
		FString LabelStr = PinLabel.IsEmpty() ? FString() : FString::Printf(TEXT("[%s] "), *PinLabel);
		Output += FString::Printf(TEXT("%s%s(cycle)\n"), *Prefix, *LabelStr);
		return;
	}
	Visited.Add(Node);

	FString Label = GetNodeLabel(Node);
	FString Prefix = FString::ChrN(Depth * 2, TEXT(' '));
	FString LabelStr = PinLabel.IsEmpty() ? FString() : FString::Printf(TEXT("[%s] "), *PinLabel);

	// Collect data input pins for context
	TArray<FString> DataParts;
	for (UEdGraphPin* Pin : Node->Pins)
	{
		if (Pin->Direction == EGPD_Input && !IsExecPin(Pin) && !IsPoseLinkPin(Pin)
			&& !Pin->bHidden && Pin->PinName != TEXT("self"))
		{
			FString DPinName = Pin->GetDisplayName().IsEmpty() ? Pin->PinName.ToString() : Pin->GetDisplayName().ToString();
			if (Pin->LinkedTo.Num() > 0)
			{
				UEdGraphNode* Src = FollowThroughKnots(Pin->LinkedTo[0]->GetOwningNode(), EGPD_Input);
				FString SrcLabel = GetNodeLabel(Src);
				DataParts.Add(FString::Printf(TEXT("%s <- %s"), *DPinName, SrcLabel.IsEmpty() ? TEXT("???") : *SrcLabel));
			}
			else
			{
				FString DefaultVal = Pin->DefaultValue.IsEmpty() ? TEXT("0") : Pin->DefaultValue;
				DataParts.Add(FString::Printf(TEXT("%s [UNWIRED=%s]"), *DPinName, *DefaultVal));
			}
		}
	}
	FString DataSuffix = DataParts.Num() > 0
		? FString::Printf(TEXT("  (%s)"), *FString::Join(DataParts, TEXT(", ")))
		: FString();

	Output += FString::Printf(TEXT("%s%s%s%s\n"), *Prefix, *LabelStr, *Label, *DataSuffix);

	// Walk pose input pins backward
	TArray<UEdGraphPin*> PoseInputs = GetPoseInputPins(Node);

	if (PoseInputs.Num() == 1)
	{
		for (UEdGraphPin* LinkedPin : PoseInputs[0]->LinkedTo)
		{
			WalkPoseChain(LinkedPin->GetOwningNode(), Depth + 1, Output, Visited);
		}
	}
	else if (PoseInputs.Num() > 1)
	{
		for (UEdGraphPin* PosePin : PoseInputs)
		{
			FString BlendLabel = GetBlendPinLabel(PosePin, Node);
			if (PosePin->LinkedTo.Num() > 0)
			{
				for (UEdGraphPin* LinkedPin : PosePin->LinkedTo)
				{
					WalkPoseChain(LinkedPin->GetOwningNode(), Depth + 1, Output, Visited, BlendLabel);
				}
			}
			else
			{
				Output += FString::Printf(TEXT("%s  [%s] (not connected)\n"), *Prefix, *BlendLabel);
			}
		}
	}
}

