FString FBlueprintGraphReader::GetDataInputSummary(UEdGraphNode* Node)
{
	TArray<UEdGraphPin*> DataPins = GetDataInputPins(Node);
	if (DataPins.Num() == 0) return FString();

	TArray<FString> Parts;
	for (UEdGraphPin* Pin : DataPins)
	{
		FString PinLabel = Pin->GetDisplayName().IsEmpty()
			? Pin->PinName.ToString()
			: Pin->GetDisplayName().ToString();

		if (Pin->LinkedTo.Num() > 0)
		{
			UEdGraphNode* SourceNode = FollowThroughKnots(Pin->LinkedTo[0]->GetOwningNode(), EGPD_Input);
			FString SourceLabel = GetNodeLabel(SourceNode);
			if (!SourceLabel.IsEmpty())
			{
				Parts.Add(FString::Printf(TEXT("%s=%s"), *PinLabel, *SourceLabel));
			}
			else
			{
				Parts.Add(FString::Printf(TEXT("%s=<connected>"), *PinLabel));
			}
		}
		else if (!Pin->DefaultValue.IsEmpty())
		{
			Parts.Add(FString::Printf(TEXT("%s=%s"), *PinLabel, *Pin->DefaultValue));
		}
	}

	if (Parts.Num() == 0) return FString();
	return FString::Printf(TEXT("  (%s)"), *FString::Join(Parts, TEXT(", ")));
}

void FBlueprintGraphReader::WalkExecChain(UEdGraphNode* Node, int32 Depth, FString& Output, TSet<UEdGraphNode*>& Visited, TArray<FString>* OutVisitedIds)
{
	if (!Node) return;

	// Resolve through knots
	Node = FollowThroughKnots(Node, EGPD_Output);
	if (!Node) return;

	if (Visited.Contains(Node))
	{
		FString Prefix = FString::ChrN(Depth * 2, TEXT(' '));
		Output += FString::Printf(TEXT("%s-> (cycle)\n"), *Prefix);
		return;
	}
	Visited.Add(Node);

	FString Label = GetNodeLabel(Node);

	// Skip nodes with no label (knots, etc.)
	if (Label.IsEmpty())
	{
		TArray<UEdGraphPin*> ExecOuts = GetExecOutputPins(Node);
		for (UEdGraphPin* ExecOut : ExecOuts)
		{
			for (UEdGraphPin* LinkedPin : ExecOut->LinkedTo)
			{
				WalkExecChain(LinkedPin->GetOwningNode(), Depth, Output, Visited, OutVisitedIds);
			}
		}
		return;
	}

	FString NodeId = FBlueprintGraphEditor::GetNodeId(Node);
	if (NodeId.IsEmpty())
	{
		NodeId = Node->GetName();
	}

	if (OutVisitedIds && !NodeId.IsEmpty())
	{
		OutVisitedIds->AddUnique(NodeId);
	}

	if (!NodeId.IsEmpty())
	{
		Label += FString::Printf(TEXT(" [id:%s]"), *NodeId);
	}

	// Append data input summary
	Label += GetDataInputSummary(Node);

	// Output this node
	FString Prefix = FString::ChrN(Depth * 2, TEXT(' '));
	if (Depth == 0)
	{
		Output += FString::Printf(TEXT("%s%s\n"), *Prefix, *Label);
	}
	else
	{
		Output += FString::Printf(TEXT("%sthen -> %s\n"), *Prefix, *Label);
	}

	// Follow exec output pins
	TArray<UEdGraphPin*> ExecOuts = GetExecOutputPins(Node);
	if (ExecOuts.Num() == 1)
	{
		for (UEdGraphPin* LinkedPin : ExecOuts[0]->LinkedTo)
		{
			WalkExecChain(LinkedPin->GetOwningNode(), Depth + 1, Output, Visited, OutVisitedIds);
		}
	}
	else if (ExecOuts.Num() > 1)
	{
		for (UEdGraphPin* ExecOut : ExecOuts)
		{
			if (ExecOut->LinkedTo.Num() == 0) continue;

			FString PinLabel = ExecOut->GetDisplayName().IsEmpty()
				? ExecOut->PinName.ToString()
				: ExecOut->GetDisplayName().ToString();

			Output += FString::Printf(TEXT("%s  [%s]:\n"), *Prefix, *PinLabel);
			for (UEdGraphPin* LinkedPin : ExecOut->LinkedTo)
			{
				WalkExecChain(LinkedPin->GetOwningNode(), Depth + 2, Output, Visited, OutVisitedIds);
			}
		}
	}
}

