TSharedPtr<FJsonObject> FBlueprintGraphReader::SerializeAnimNodeInfo(UEdGraphNode* Node, UEdGraph* Graph)
{
	TSharedPtr<FJsonObject> Json = MakeShared<FJsonObject>();
	if (!Node) return Json;

	FString NodeId = GetOrAssignNodeId(Node, Graph);
	Json->SetStringField(TEXT("node_id"), NodeId);
	Json->SetStringField(TEXT("node_class"), Node->GetClass()->GetName());
	Json->SetStringField(TEXT("label"), GetAnimNodeLabel(Node));
	if (Graph)
	{
		Json->SetStringField(TEXT("graph_name"), Graph->GetName());
	}
	Json->SetNumberField(TEXT("pos_x"), Node->NodePosX);
	Json->SetNumberField(TEXT("pos_y"), Node->NodePosY);

	TSharedPtr<FJsonObject> AnimProps = MakeShared<FJsonObject>();

	if (UAnimGraphNode_SequencePlayer* SeqPlayer = Cast<UAnimGraphNode_SequencePlayer>(Node))
	{
		AnimProps->SetStringField(TEXT("type"), TEXT("SequencePlayer"));
		UAnimationAsset* Seq = SeqPlayer->Node.GetSequence();
		if (Seq)
		{
			AnimProps->SetStringField(TEXT("animation"), Seq->GetPathName());
		}
	}
	else if (UAnimGraphNode_BlendSpacePlayer* BSPlayer = Cast<UAnimGraphNode_BlendSpacePlayer>(Node))
	{
		AnimProps->SetStringField(TEXT("type"), TEXT("BlendSpacePlayer"));
		UBlendSpace* BS = Cast<UBlendSpace>(BSPlayer->Node.GetBlendSpace());
		if (BS)
		{
			AnimProps->SetStringField(TEXT("blend_space"), BS->GetPathName());
		}
	}
	else if (UAnimGraphNode_StateMachine* SM = Cast<UAnimGraphNode_StateMachine>(Node))
	{
		AnimProps->SetStringField(TEXT("type"), TEXT("StateMachine"));
		AnimProps->SetStringField(TEXT("machine_name"), SM->GetStateMachineName());
	}
	else if (UAnimGraphNode_Slot* Slot = Cast<UAnimGraphNode_Slot>(Node))
	{
		AnimProps->SetStringField(TEXT("type"), TEXT("Slot"));
		AnimProps->SetStringField(TEXT("slot_name"), Slot->Node.SlotName.ToString());
	}
	else if (UAnimGraphNode_SaveCachedPose* SavePose = Cast<UAnimGraphNode_SaveCachedPose>(Node))
	{
		AnimProps->SetStringField(TEXT("type"), TEXT("SaveCachedPose"));
		AnimProps->SetStringField(TEXT("cache_name"), SavePose->CacheName);
	}
	else if (UAnimGraphNode_UseCachedPose* UsePose = Cast<UAnimGraphNode_UseCachedPose>(Node))
	{
		AnimProps->SetStringField(TEXT("type"), TEXT("UseCachedPose"));
		AnimProps->SetStringField(TEXT("cache_name"), UsePose->GetNodeTitle(ENodeTitleType::ListView).ToString());
	}
	else if (UAnimGraphNode_LinkedAnimLayer* LayerNode = Cast<UAnimGraphNode_LinkedAnimLayer>(Node))
	{
		AnimProps->SetStringField(TEXT("type"), TEXT("LinkedAnimLayer"));
		AnimProps->SetStringField(TEXT("layer"), LayerNode->Node.Layer.ToString());
		UClass* InterfaceClass = *LayerNode->Node.Interface;
		if (InterfaceClass)
		{
			AnimProps->SetStringField(TEXT("interface_class"), InterfaceClass->GetName());
		}
	}
	else if (UAnimGraphNode_LinkedAnimGraph* LinkedGraph = Cast<UAnimGraphNode_LinkedAnimGraph>(Node))
	{
		AnimProps->SetStringField(TEXT("type"), TEXT("LinkedAnimGraph"));
	}
	else if (Cast<UAnimGraphNode_LayeredBoneBlend>(Node))
	{
		AnimProps->SetStringField(TEXT("type"), TEXT("LayeredBoneBlend"));
	}
	else if (Cast<UAnimGraphNode_BlendListByBool>(Node))
	{
		AnimProps->SetStringField(TEXT("type"), TEXT("BlendListByBool"));
	}
	else if (Cast<UAnimGraphNode_BlendListByEnum>(Node))
	{
		AnimProps->SetStringField(TEXT("type"), TEXT("BlendListByEnum"));
	}
	else if (Cast<UAnimGraphNode_Root>(Node))
	{
		AnimProps->SetStringField(TEXT("type"), TEXT("Root"));
	}
	else if (Cast<UAnimGraphNode_StateResult>(Node))
	{
		AnimProps->SetStringField(TEXT("type"), TEXT("StateResult"));
	}
	else if (Cast<UAnimGraphNode_TransitionResult>(Node))
	{
		AnimProps->SetStringField(TEXT("type"), TEXT("TransitionResult"));
	}
	else
	{
		AnimProps->SetStringField(TEXT("type"), Node->GetClass()->GetName());
	}

	Json->SetObjectField(TEXT("anim_properties"), AnimProps);

	TArray<TSharedPtr<FJsonValue>> PinsArray;
	for (UEdGraphPin* Pin : Node->Pins)
	{
		if (!Pin || Pin->bHidden) continue;

		TSharedPtr<FJsonObject> PinJson = MakeShared<FJsonObject>();
		PinJson->SetStringField(TEXT("name"), Pin->PinName.ToString());
		PinJson->SetStringField(TEXT("direction"), Pin->Direction == EGPD_Input ? TEXT("Input") : TEXT("Output"));

		FString PinCategory = Pin->PinType.PinCategory.ToString();
		if (IsPoseLinkPin(Pin))
		{
			PinCategory = TEXT("PoseLink");
		}
		PinJson->SetStringField(TEXT("type"), PinCategory);
		PinJson->SetBoolField(TEXT("connected"), Pin->LinkedTo.Num() > 0);

		if (Pin->LinkedTo.Num() > 0)
		{
			TArray<TSharedPtr<FJsonValue>> LinkedArray;
			for (UEdGraphPin* Linked : Pin->LinkedTo)
			{
				if (!Linked) continue;
				UEdGraphNode* LinkedNode = Linked->GetOwningNode();
				if (!LinkedNode) continue;

				FString LinkedId = FBlueprintGraphEditor::GetNodeId(LinkedNode);
				if (LinkedId.IsEmpty()) LinkedId = LinkedNode->GetName();

				TSharedPtr<FJsonObject> LinkJson = MakeShared<FJsonObject>();
				LinkJson->SetStringField(TEXT("node_id"), LinkedId);
				LinkJson->SetStringField(TEXT("pin_name"), Linked->PinName.ToString());
				LinkedArray.Add(MakeShared<FJsonValueObject>(LinkJson));
			}
			PinJson->SetArrayField(TEXT("linked_to"), LinkedArray);
		}

		if (!Pin->DefaultValue.IsEmpty())
		{
			PinJson->SetStringField(TEXT("default_value"), Pin->DefaultValue);
		}

		PinsArray.Add(MakeShared<FJsonValueObject>(PinJson));
	}
	Json->SetArrayField(TEXT("pins"), PinsArray);

	return Json;
}

// ============================================================================
// State-Bound Graph Discovery
// ============================================================================

TArray<FBlueprintGraphReader::FStateBoundGraph> FBlueprintGraphReader::CollectStateBoundGraphs(UBlueprint* Blueprint)
{
	TArray<FStateBoundGraph> Result;
	if (!Blueprint) return Result;

	TArray<UEdGraph*> AllGraphs;
	Blueprint->GetAllGraphs(AllGraphs);

	for (UEdGraph* Graph : AllGraphs)
	{
		if (!Graph) continue;
		for (UEdGraphNode* Node : Graph->Nodes)
		{
			UAnimGraphNode_StateMachine* SMNode = Cast<UAnimGraphNode_StateMachine>(Node);
			if (!SMNode || !SMNode->EditorStateMachineGraph) continue;

			FString SMName = SMNode->GetStateMachineName();
			for (UEdGraphNode* SMChild : SMNode->EditorStateMachineGraph->Nodes)
			{
				UAnimStateNode* StateNode = Cast<UAnimStateNode>(SMChild);
				if (!StateNode) continue;

				UEdGraph* BoundGraph = StateNode->GetBoundGraph();
				if (!BoundGraph) continue;

				FStateBoundGraph Entry;
				Entry.Graph = BoundGraph;
				Entry.StateMachineName = SMName;
				Entry.StateName = StateNode->GetNodeTitle(ENodeTitleType::ListView).ToString();
				Result.Add(Entry);
			}
		}
	}

	return Result;
}

// ============================================================================
// Cross-Graph Node Lookup
// ============================================================================

FBlueprintGraphReader::FNodeSearchResult FBlueprintGraphReader::FindNodeByIdAcrossGraphs(
	UBlueprint* Blueprint, const FString& NodeId, const FString& GraphNameHint)
{
	FNodeSearchResult Result;
	if (!Blueprint || NodeId.IsEmpty()) return Result;

	TArray<UEdGraph*> AllGraphs;
	Blueprint->GetAllGraphs(AllGraphs);

	auto SearchGraph = [&](UEdGraph* Graph) -> bool
	{
		if (!Graph) return false;
		UEdGraphNode* Found = FBlueprintGraphEditor::FindNodeById(Graph, NodeId);
		if (Found)
		{
			Result.Node = Found;
			Result.Graph = Graph;
			Result.GraphName = Graph->GetName();
			Result.GraphType = GraphTypeToString(ClassifyGraph(Graph));
			return true;
		}
		return false;
	};

	if (!GraphNameHint.IsEmpty())
	{
		for (UEdGraph* Graph : AllGraphs)
		{
			if (Graph && Graph->GetName() == GraphNameHint)
			{
				if (SearchGraph(Graph)) return Result;
				break;
			}
		}
	}

	for (UEdGraph* Graph : AllGraphs)
	{
		if (SearchGraph(Graph)) return Result;
	}

	// Fallback: search state-bound graphs (not included in GetAllGraphs)
	for (const FStateBoundGraph& SBG : CollectStateBoundGraphs(Blueprint))
	{
		UEdGraphNode* Found = FBlueprintGraphEditor::FindNodeById(SBG.Graph, NodeId);
		if (Found)
		{
			Result.Node = Found;
			Result.Graph = SBG.Graph;
			Result.GraphName = SBG.Graph->GetName();
			Result.GraphType = TEXT("StateGraph");
			return Result;
		}
	}

	return Result;
}

// ============================================================================
// find_nodes
// ============================================================================

TSharedPtr<FJsonObject> FBlueprintGraphReader::FindNodes(UBlueprint* Blueprint,
	const FString& Search, const FString& NodeClass, const FString& GraphName, const FString& GraphType)
{
	TSharedPtr<FJsonObject> Response = MakeShared<FJsonObject>();
	if (!Blueprint)
	{
		Response->SetStringField(TEXT("error"), TEXT("null blueprint"));
		return Response;
	}

	TArray<UEdGraph*> AllGraphs;
	Blueprint->GetAllGraphs(AllGraphs);

	TArray<TSharedPtr<FJsonValue>> ResultsArray;

	for (UEdGraph* Graph : AllGraphs)
	{
		if (!Graph) continue;

		if (!GraphName.IsEmpty() && Graph->GetName() != GraphName)
		{
			continue;
		}

		EGraphType GType = ClassifyGraph(Graph);
		FString GTypeStr = GraphTypeToString(GType);

		if (!GraphType.IsEmpty() && !GraphType.Equals(TEXT("All"), ESearchCase::IgnoreCase))
		{
			if (!GTypeStr.Equals(GraphType, ESearchCase::IgnoreCase))
			{
				continue;
			}
		}

		for (UEdGraphNode* Node : Graph->Nodes)
		{
			if (!Node) continue;
			if (Cast<UK2Node_Knot>(Node)) continue;
			if (Node->GetClass()->GetName().Contains(TEXT("Comment"))) continue;

			if (!NodeClass.IsEmpty())
			{
				FString ClassName = Node->GetClass()->GetName();
				if (!ClassName.Contains(NodeClass, ESearchCase::IgnoreCase))
				{
					continue;
				}
			}

			FString Label = GetNodeLabel(Node);

			if (!Search.IsEmpty())
			{
				if (!Label.Contains(Search, ESearchCase::IgnoreCase))
				{
					continue;
				}
			}

			FString NodeId = GetOrAssignNodeId(Node, Graph);

			int32 PinCount = 0;
			int32 ConnectedPinCount = 0;
			for (UEdGraphPin* Pin : Node->Pins)
			{
				if (Pin && !Pin->bHidden)
				{
					PinCount++;
					if (Pin->LinkedTo.Num() > 0) ConnectedPinCount++;
				}
			}

			TSharedPtr<FJsonObject> NodeJson = MakeShared<FJsonObject>();
			NodeJson->SetStringField(TEXT("node_id"), NodeId);
			NodeJson->SetStringField(TEXT("label"), Label);
			NodeJson->SetStringField(TEXT("node_class"), Node->GetClass()->GetName());
			NodeJson->SetStringField(TEXT("graph_name"), Graph->GetName());
			NodeJson->SetStringField(TEXT("graph_type"), GTypeStr);
			NodeJson->SetNumberField(TEXT("pos_x"), Node->NodePosX);
			NodeJson->SetNumberField(TEXT("pos_y"), Node->NodePosY);
			NodeJson->SetNumberField(TEXT("pin_count"), PinCount);
			NodeJson->SetNumberField(TEXT("connected_pin_count"), ConnectedPinCount);

			ResultsArray.Add(MakeShared<FJsonValueObject>(NodeJson));
		}
	}

	// Also search state-bound graphs
	bool bWantStateGraphs = GraphType.IsEmpty()
		|| GraphType.Equals(TEXT("All"), ESearchCase::IgnoreCase)
		|| GraphType.Equals(TEXT("AnimGraph"), ESearchCase::IgnoreCase)
		|| GraphType.Equals(TEXT("StateGraph"), ESearchCase::IgnoreCase);

	if (bWantStateGraphs && GraphName.IsEmpty())
	{
		for (const FStateBoundGraph& SBG : CollectStateBoundGraphs(Blueprint))
		{
			for (UEdGraphNode* Node : SBG.Graph->Nodes)
			{
				if (!Node) continue;
				if (Node->GetClass()->GetName().Contains(TEXT("Comment"))) continue;

				if (!NodeClass.IsEmpty())
				{
					FString ClassName = Node->GetClass()->GetName();
					if (!ClassName.Contains(NodeClass, ESearchCase::IgnoreCase))
					{
						continue;
					}
				}

				FString Label = GetNodeLabel(Node);
				if (!Search.IsEmpty() && !Label.Contains(Search, ESearchCase::IgnoreCase))
				{
					continue;
				}

				FString NodeId = GetOrAssignNodeId(Node, SBG.Graph);

				int32 PinCount = 0;
				int32 ConnectedPinCount = 0;
				for (UEdGraphPin* Pin : Node->Pins)
				{
					if (Pin && !Pin->bHidden)
					{
						PinCount++;
						if (Pin->LinkedTo.Num() > 0) ConnectedPinCount++;
					}
				}

				TSharedPtr<FJsonObject> NodeJson = MakeShared<FJsonObject>();
				NodeJson->SetStringField(TEXT("node_id"), NodeId);
				NodeJson->SetStringField(TEXT("label"), Label);
				NodeJson->SetStringField(TEXT("node_class"), Node->GetClass()->GetName());
				NodeJson->SetStringField(TEXT("graph_name"), SBG.Graph->GetName());
				NodeJson->SetStringField(TEXT("graph_type"), TEXT("StateGraph"));
				NodeJson->SetStringField(TEXT("state_machine"), SBG.StateMachineName);
				NodeJson->SetStringField(TEXT("state_name"), SBG.StateName);
				NodeJson->SetNumberField(TEXT("pos_x"), Node->NodePosX);
				NodeJson->SetNumberField(TEXT("pos_y"), Node->NodePosY);
				NodeJson->SetNumberField(TEXT("pin_count"), PinCount);
				NodeJson->SetNumberField(TEXT("connected_pin_count"), ConnectedPinCount);

				ResultsArray.Add(MakeShared<FJsonValueObject>(NodeJson));
			}
		}
	}

	Response->SetArrayField(TEXT("nodes"), ResultsArray);
	Response->SetNumberField(TEXT("count"), ResultsArray.Num());
	return Response;
}

// ============================================================================
// get_node
// ============================================================================

TSharedPtr<FJsonObject> FBlueprintGraphReader::GetNode(UBlueprint* Blueprint,
	const FString& NodeId, const FString& GraphName)
{
	TSharedPtr<FJsonObject> Response = MakeShared<FJsonObject>();
	if (!Blueprint)
	{
		Response->SetStringField(TEXT("error"), TEXT("null blueprint"));
		return Response;
	}

	FNodeSearchResult Found = FindNodeByIdAcrossGraphs(Blueprint, NodeId, GraphName);
	if (!Found.Node)
	{
		Response->SetStringField(TEXT("error"), FString::Printf(TEXT("Node '%s' not found"), *NodeId));
		return Response;
	}

	if (Cast<UAnimGraphNode_Base>(Found.Node) || Cast<UAnimStateNodeBase>(Found.Node) || Cast<UAnimStateEntryNode>(Found.Node))
	{
		Response = SerializeAnimNodeInfo(Found.Node, Found.Graph);
	}
	else
	{
		Response = SerializeK2NodeInfo(Found.Node, Found.Graph);
	}

	Response->SetStringField(TEXT("graph_name"), Found.GraphName);
	Response->SetStringField(TEXT("graph_type"), Found.GraphType);

	return Response;
}

// ============================================================================
// verify_connection
// ============================================================================

TSharedPtr<FJsonObject> FBlueprintGraphReader::VerifyConnection(UBlueprint* Blueprint,
	const FString& SourceNodeId, const FString& TargetNodeId,
	const FString& SourcePin, const FString& TargetPin)
{
	TSharedPtr<FJsonObject> Response = MakeShared<FJsonObject>();
	if (!Blueprint)
	{
		Response->SetStringField(TEXT("error"), TEXT("null blueprint"));
		return Response;
	}

	FNodeSearchResult SourceResult = FindNodeByIdAcrossGraphs(Blueprint, SourceNodeId);
	if (!SourceResult.Node)
	{
		Response->SetStringField(TEXT("error"), FString::Printf(TEXT("Source node '%s' not found"), *SourceNodeId));
		return Response;
	}

	FNodeSearchResult TargetResult = FindNodeByIdAcrossGraphs(Blueprint, TargetNodeId);
	if (!TargetResult.Node)
	{
		Response->SetStringField(TEXT("error"), FString::Printf(TEXT("Target node '%s' not found"), *TargetNodeId));
		return Response;
	}

	Response->SetBoolField(TEXT("connected"), false);
	Response->SetStringField(TEXT("source_label"), GetNodeLabel(SourceResult.Node));
	Response->SetStringField(TEXT("target_label"), GetNodeLabel(TargetResult.Node));

	for (UEdGraphPin* SPin : SourceResult.Node->Pins)
	{
		if (!SPin || SPin->Direction != EGPD_Output) continue;
		if (!SourcePin.IsEmpty() && !SPin->PinName.ToString().Equals(SourcePin, ESearchCase::IgnoreCase)) continue;

		for (UEdGraphPin* Linked : SPin->LinkedTo)
		{
			if (!Linked) continue;
			UEdGraphNode* LinkedNode = Linked->GetOwningNode();
			if (!LinkedNode) continue;

			UEdGraphNode* ResolvedNode = FollowThroughKnots(LinkedNode, EGPD_Output);

			bool bMatch = (ResolvedNode == TargetResult.Node) || (LinkedNode == TargetResult.Node);
			if (!bMatch) continue;

			if (!TargetPin.IsEmpty() && !Linked->PinName.ToString().Equals(TargetPin, ESearchCase::IgnoreCase)) continue;

			Response->SetBoolField(TEXT("connected"), true);
			Response->SetStringField(TEXT("source_pin"), SPin->PinName.ToString());
			Response->SetStringField(TEXT("target_pin"), Linked->PinName.ToString());
			return Response;
		}
	}

	for (UEdGraphPin* SPin : SourceResult.Node->Pins)
	{
		if (!SPin || SPin->Direction != EGPD_Input) continue;
		if (!SourcePin.IsEmpty() && !SPin->PinName.ToString().Equals(SourcePin, ESearchCase::IgnoreCase)) continue;

		for (UEdGraphPin* Linked : SPin->LinkedTo)
		{
			if (!Linked) continue;
			UEdGraphNode* LinkedNode = Linked->GetOwningNode();
			if (!LinkedNode) continue;

			UEdGraphNode* ResolvedNode = FollowThroughKnots(LinkedNode, EGPD_Input);

			bool bMatch = (ResolvedNode == TargetResult.Node) || (LinkedNode == TargetResult.Node);
			if (!bMatch) continue;

			if (!TargetPin.IsEmpty() && !Linked->PinName.ToString().Equals(TargetPin, ESearchCase::IgnoreCase)) continue;

			Response->SetBoolField(TEXT("connected"), true);
			Response->SetStringField(TEXT("source_pin"), SPin->PinName.ToString());
			Response->SetStringField(TEXT("target_pin"), Linked->PinName.ToString());
			return Response;
		}
	}

	return Response;
}

// ============================================================================
// get_node_connections
// ============================================================================

TSharedPtr<FJsonObject> FBlueprintGraphReader::GetNodeConnections(UBlueprint* Blueprint,
	const FString& NodeId, const FString& Direction, const FString& PinFilter)
{
	TSharedPtr<FJsonObject> Response = MakeShared<FJsonObject>();
	if (!Blueprint)
	{
		Response->SetStringField(TEXT("error"), TEXT("null blueprint"));
		return Response;
	}

	FNodeSearchResult Found = FindNodeByIdAcrossGraphs(Blueprint, NodeId);
	if (!Found.Node)
	{
		Response->SetStringField(TEXT("error"), FString::Printf(TEXT("Node '%s' not found"), *NodeId));
		return Response;
	}

	TSet<FString> AllowedPins;
	if (!PinFilter.IsEmpty())
	{
		TArray<FString> FilterParts;
		PinFilter.ParseIntoArray(FilterParts, TEXT(","));
		for (FString& Part : FilterParts)
		{
			AllowedPins.Add(Part.TrimStartAndEnd());
		}
	}

	bool bWantInput = Direction.IsEmpty() || Direction.Equals(TEXT("both"), ESearchCase::IgnoreCase)
		|| Direction.Equals(TEXT("input"), ESearchCase::IgnoreCase);
	bool bWantOutput = Direction.IsEmpty() || Direction.Equals(TEXT("both"), ESearchCase::IgnoreCase)
		|| Direction.Equals(TEXT("output"), ESearchCase::IgnoreCase);

	Response->SetStringField(TEXT("node_id"), GetOrAssignNodeId(Found.Node, Found.Graph));
	Response->SetStringField(TEXT("label"), GetNodeLabel(Found.Node));

	TSharedPtr<FJsonObject> Connections = MakeShared<FJsonObject>();
	TArray<TSharedPtr<FJsonValue>> InputConns, OutputConns;
	TArray<TSharedPtr<FJsonValue>> UnconnectedInput, UnconnectedOutput;

	for (UEdGraphPin* Pin : Found.Node->Pins)
	{
		if (!Pin || Pin->bHidden) continue;

		FString PinName = Pin->PinName.ToString();
		if (AllowedPins.Num() > 0 && !AllowedPins.Contains(PinName)) continue;

		bool bIsInput = Pin->Direction == EGPD_Input;
		if (bIsInput && !bWantInput) continue;
		if (!bIsInput && !bWantOutput) continue;

		if (Pin->LinkedTo.Num() == 0)
		{
			TSharedPtr<FJsonObject> UnconnJson = MakeShared<FJsonObject>();
			UnconnJson->SetStringField(TEXT("pin_name"), PinName);

			FString PinCategory = Pin->PinType.PinCategory.ToString();
			if (IsPoseLinkPin(Pin)) PinCategory = TEXT("PoseLink");
			else if (IsExecPin(Pin)) PinCategory = TEXT("Exec");
			UnconnJson->SetStringField(TEXT("pin_type"), PinCategory);

			if (!Pin->DefaultValue.IsEmpty())
			{
				UnconnJson->SetStringField(TEXT("default_value"), Pin->DefaultValue);
			}

			if (bIsInput)
				UnconnectedInput.Add(MakeShared<FJsonValueObject>(UnconnJson));
			else
				UnconnectedOutput.Add(MakeShared<FJsonValueObject>(UnconnJson));
			continue;
		}

		for (UEdGraphPin* Linked : Pin->LinkedTo)
		{
			if (!Linked) continue;
			UEdGraphNode* LinkedNode = Linked->GetOwningNode();
			if (!LinkedNode) continue;

			UEdGraphNode* Resolved = FollowThroughKnots(LinkedNode,
				bIsInput ? EGPD_Input : EGPD_Output);
			if (Resolved) LinkedNode = Resolved;

			FString LinkedId = FBlueprintGraphEditor::GetNodeId(LinkedNode);
			if (LinkedId.IsEmpty()) LinkedId = LinkedNode->GetName();

			TSharedPtr<FJsonObject> ConnJson = MakeShared<FJsonObject>();
			ConnJson->SetStringField(TEXT("pin_name"), PinName);

			FString PinCategory = Pin->PinType.PinCategory.ToString();
			if (IsPoseLinkPin(Pin)) PinCategory = TEXT("PoseLink");
			else if (IsExecPin(Pin)) PinCategory = TEXT("Exec");
			ConnJson->SetStringField(TEXT("pin_type"), PinCategory);

			TSharedPtr<FJsonObject> ConnectedTo = MakeShared<FJsonObject>();
			ConnectedTo->SetStringField(TEXT("node_id"), LinkedId);
			ConnectedTo->SetStringField(TEXT("pin_name"), Linked->PinName.ToString());
			ConnectedTo->SetStringField(TEXT("label"), GetNodeLabel(LinkedNode));
			ConnJson->SetObjectField(TEXT("connected_to"), ConnectedTo);

			if (bIsInput)
				InputConns.Add(MakeShared<FJsonValueObject>(ConnJson));
			else
				OutputConns.Add(MakeShared<FJsonValueObject>(ConnJson));
		}
	}

	Connections->SetArrayField(TEXT("input"), InputConns);
	Connections->SetArrayField(TEXT("output"), OutputConns);
	Response->SetObjectField(TEXT("connections"), Connections);

	TSharedPtr<FJsonObject> Unconnected = MakeShared<FJsonObject>();
	Unconnected->SetArrayField(TEXT("input"), UnconnectedInput);
	Unconnected->SetArrayField(TEXT("output"), UnconnectedOutput);
	Response->SetObjectField(TEXT("unconnected_pins"), Unconnected);

	return Response;
}

// ============================================================================
// get_graph_summary
// ============================================================================

TSharedPtr<FJsonObject> FBlueprintGraphReader::GetGraphSummary(UBlueprint* Blueprint,
	const FString& GraphName, const FString& GraphType)
{
	TSharedPtr<FJsonObject> Response = MakeShared<FJsonObject>();
	if (!Blueprint)
	{
		Response->SetStringField(TEXT("error"), TEXT("null blueprint"));
		return Response;
	}

	TArray<UEdGraph*> AllGraphs;
	Blueprint->GetAllGraphs(AllGraphs);

	TArray<TSharedPtr<FJsonValue>> GraphsArray;

	for (UEdGraph* Graph : AllGraphs)
	{
		if (!Graph) continue;

		if (!GraphName.IsEmpty() && Graph->GetName() != GraphName)
		{
			continue;
		}

		EGraphType GType = ClassifyGraph(Graph);
		FString GTypeStr = GraphTypeToString(GType);

		if (!GraphType.IsEmpty() && !GraphType.Equals(TEXT("All"), ESearchCase::IgnoreCase))
		{
			if (!GTypeStr.Equals(GraphType, ESearchCase::IgnoreCase))
			{
				continue;
			}
		}

		TSharedPtr<FJsonObject> GraphJson = MakeShared<FJsonObject>();
		GraphJson->SetStringField(TEXT("name"), Graph->GetName());
		GraphJson->SetStringField(TEXT("type"), GTypeStr);

		TMap<FString, int32> NodesByType;
		TArray<TSharedPtr<FJsonValue>> EntryPoints;
		TArray<TSharedPtr<FJsonValue>> AllNodeIds;
		int32 TotalNodes = 0;

		for (UEdGraphNode* Node : Graph->Nodes)
		{
			if (!Node) continue;
			if (Cast<UK2Node_Knot>(Node)) continue;
			if (Node->GetClass()->GetName().Contains(TEXT("Comment"))) continue;

			TotalNodes++;

			FString NodeId = GetOrAssignNodeId(Node, Graph);
			AllNodeIds.Add(MakeShared<FJsonValueString>(NodeId));

			FString TypeBucket;
			if (Cast<UK2Node_Event>(Node) || Cast<UK2Node_CustomEvent>(Node))
			{
				TypeBucket = TEXT("Event");
			}
			else if (Cast<UK2Node_FunctionEntry>(Node))
			{
				TypeBucket = TEXT("FunctionEntry");
			}
			else if (Cast<UK2Node_CallFunction>(Node))
			{
				TypeBucket = TEXT("CallFunction");
			}
			else if (Cast<UK2Node_VariableGet>(Node))
			{
				TypeBucket = TEXT("VarGet");
			}
			else if (Cast<UK2Node_VariableSet>(Node))
			{
				TypeBucket = TEXT("VarSet");
			}
			else if (Cast<UK2Node_IfThenElse>(Node))
			{
				TypeBucket = TEXT("Branch");
			}
			else if (Cast<UAnimGraphNode_Root>(Node))
			{
				TypeBucket = TEXT("AnimRoot");
			}
			else if (Cast<UAnimGraphNode_StateMachine>(Node))
			{
				TypeBucket = TEXT("StateMachine");
			}
			else if (Cast<UAnimGraphNode_Base>(Node))
			{
				TypeBucket = TEXT("AnimNode");
			}
			else if (Cast<UAnimStateNode>(Node))
			{
				TypeBucket = TEXT("State");
			}
			else if (Cast<UAnimStateTransitionNode>(Node))
			{
				TypeBucket = TEXT("Transition");
			}
			else if (Cast<UAnimStateEntryNode>(Node))
			{
				TypeBucket = TEXT("Entry");
			}
			else
			{
				TypeBucket = Node->GetClass()->GetName();
			}

			NodesByType.FindOrAdd(TypeBucket)++;

			bool bIsEntry = Cast<UK2Node_Event>(Node) || Cast<UK2Node_CustomEvent>(Node)
				|| Cast<UK2Node_FunctionEntry>(Node) || Cast<UAnimGraphNode_Root>(Node)
				|| Cast<UAnimStateEntryNode>(Node);

			if (bIsEntry)
			{
				TSharedPtr<FJsonObject> EntryJson = MakeShared<FJsonObject>();
				EntryJson->SetStringField(TEXT("node_id"), NodeId);
				EntryJson->SetStringField(TEXT("label"), GetNodeLabel(Node));
				EntryPoints.Add(MakeShared<FJsonValueObject>(EntryJson));
			}
		}

		GraphJson->SetNumberField(TEXT("total_nodes"), TotalNodes);

		TSharedPtr<FJsonObject> NodesByTypeJson = MakeShared<FJsonObject>();
		for (auto& Pair : NodesByType)
		{
			NodesByTypeJson->SetNumberField(Pair.Key, Pair.Value);
		}
		GraphJson->SetObjectField(TEXT("nodes_by_type"), NodesByTypeJson);
		GraphJson->SetArrayField(TEXT("entry_points"), EntryPoints);
		GraphJson->SetArrayField(TEXT("all_node_ids"), AllNodeIds);

		GraphsArray.Add(MakeShared<FJsonValueObject>(GraphJson));
	}

	// Include state-bound graphs when appropriate
	bool bWantStateGraphs = GraphType.IsEmpty()
		|| GraphType.Equals(TEXT("All"), ESearchCase::IgnoreCase)
		|| GraphType.Equals(TEXT("AnimGraph"), ESearchCase::IgnoreCase)
		|| GraphType.Equals(TEXT("StateGraph"), ESearchCase::IgnoreCase);

	if (bWantStateGraphs && GraphName.IsEmpty())
	{
		for (const FStateBoundGraph& SBG : CollectStateBoundGraphs(Blueprint))
		{
			TSharedPtr<FJsonObject> GraphJson = MakeShared<FJsonObject>();
			GraphJson->SetStringField(TEXT("name"), SBG.Graph->GetName());
			GraphJson->SetStringField(TEXT("type"), TEXT("StateGraph"));
			GraphJson->SetStringField(TEXT("state_machine"), SBG.StateMachineName);
			GraphJson->SetStringField(TEXT("state_name"), SBG.StateName);

			TMap<FString, int32> NodesByType;
			TArray<TSharedPtr<FJsonValue>> AllNodeIds;
			int32 TotalNodes = 0;

			for (UEdGraphNode* Node : SBG.Graph->Nodes)
			{
				if (!Node) continue;
				if (Node->GetClass()->GetName().Contains(TEXT("Comment"))) continue;

				TotalNodes++;
				FString NodeId = GetOrAssignNodeId(Node, SBG.Graph);
				AllNodeIds.Add(MakeShared<FJsonValueString>(NodeId));

				FString TypeBucket;
				if (Cast<UAnimGraphNode_StateResult>(Node))
					TypeBucket = TEXT("StateResult");
				else if (Cast<UAnimGraphNode_Base>(Node))
					TypeBucket = TEXT("AnimNode");
				else if (Cast<UK2Node_VariableGet>(Node))
					TypeBucket = TEXT("VarGet");
				else if (Cast<UK2Node>(Node))
					TypeBucket = TEXT("K2Node");
				else
					TypeBucket = Node->GetClass()->GetName();

				NodesByType.FindOrAdd(TypeBucket)++;
			}

			GraphJson->SetNumberField(TEXT("total_nodes"), TotalNodes);

			TSharedPtr<FJsonObject> NodesByTypeJson = MakeShared<FJsonObject>();
			for (auto& Pair : NodesByType)
			{
				NodesByTypeJson->SetNumberField(Pair.Key, Pair.Value);
			}
			GraphJson->SetObjectField(TEXT("nodes_by_type"), NodesByTypeJson);
			GraphJson->SetArrayField(TEXT("entry_points"), TArray<TSharedPtr<FJsonValue>>());
			GraphJson->SetArrayField(TEXT("all_node_ids"), AllNodeIds);

			GraphsArray.Add(MakeShared<FJsonValueObject>(GraphJson));
		}
	}

	Response->SetArrayField(TEXT("graphs"), GraphsArray);
	Response->SetNumberField(TEXT("graph_count"), GraphsArray.Num());
	return Response;
}

// ============================================================================
// get_exec_chain
// ============================================================================

TSharedPtr<FJsonObject> FBlueprintGraphReader::GetExecChain(UBlueprint* Blueprint,
	const FString& NodeId, int32 MaxDepth)
{
	TSharedPtr<FJsonObject> Response = MakeShared<FJsonObject>();
	if (!Blueprint)
	{
		Response->SetStringField(TEXT("error"), TEXT("null blueprint"));
		return Response;
	}

	FNodeSearchResult Found = FindNodeByIdAcrossGraphs(Blueprint, NodeId);
	if (!Found.Node)
	{
		Response->SetStringField(TEXT("error"), FString::Printf(TEXT("Node '%s' not found"), *NodeId));
		return Response;
	}

	Response->SetStringField(TEXT("start_node"), NodeId);
	Response->SetStringField(TEXT("start_label"), GetNodeLabel(Found.Node));

	FString Output;
	TSet<UEdGraphNode*> Visited;
	TArray<FString> VisitedIds;

	WalkExecChain(Found.Node, 0, Output, Visited, &VisitedIds);

	Response->SetStringField(TEXT("chain_summary"), Output);

	TArray<TSharedPtr<FJsonValue>> IdsArray;
	for (const FString& Id : VisitedIds)
	{
		IdsArray.Add(MakeShared<FJsonValueString>(Id));
	}
	Response->SetArrayField(TEXT("nodes_in_chain"), IdsArray);
	Response->SetNumberField(TEXT("node_count"), VisitedIds.Num());
	Response->SetBoolField(TEXT("max_depth_reached"), Visited.Num() >= MaxDepth);

	return Response;
}
