FString FBlueprintGraphReader::GraphTypeToString(EGraphType Type)
{
	switch (Type)
	{
	case EGraphType::EventGraph: return TEXT("EventGraph");
	case EGraphType::AnimGraph: return TEXT("AnimGraph");
	case EGraphType::StateMachine: return TEXT("StateMachine");
	default: return TEXT("Other");
	}
}

FString FBlueprintGraphReader::GetOrAssignNodeId(UEdGraphNode* Node, UEdGraph* Graph)
{
	if (!Node) return FString();

	FString NodeId = FBlueprintGraphEditor::GetNodeId(Node);
	if (!NodeId.IsEmpty()) return NodeId;

	if (Cast<UK2Node>(Node))
	{
		FString NodeType, Context;
		DeriveK2NodeTypeAndContext(Node, NodeType, Context);
		NodeId = FBlueprintGraphEditor::GenerateNodeId(NodeType, Context, Graph);
		FBlueprintGraphEditor::SetNodeId(Node, NodeId);
		return NodeId;
	}

	return Node->GetName();
}

