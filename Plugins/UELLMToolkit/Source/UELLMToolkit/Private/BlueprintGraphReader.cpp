FString FBlueprintGraphReader::ResolveOperatorSymbol(const FString& FuncName)
{
	const TMap<FString, FString>& OpMap = GetOperatorMap();

	if (const FString* Symbol = OpMap.Find(FuncName))
	{
		return *Symbol;
	}

	// Try stripping type suffix (e.g., "Greater_FloatFloat" -> "Greater")
	int32 UnderscoreIdx;
	if (FuncName.FindChar(TEXT('_'), UnderscoreIdx))
	{
		FString Base = FuncName.Left(UnderscoreIdx);
		if (const FString* Symbol = OpMap.Find(Base))
		{
			return *Symbol;
		}
	}

	return FString();
}

FString FBlueprintGraphReader::BuildExpression(UEdGraphNode* Node, TSet<UEdGraphNode*>& Visited)
{
	if (!Node) return TEXT("???");

	if (Visited.Contains(Node))
	{
		return TEXT("(cycle)");
	}
	Visited.Add(Node);

	// Knots — follow through
	if (UK2Node_Knot* Knot = Cast<UK2Node_Knot>(Node))
	{
		for (UEdGraphPin* Pin : Knot->Pins)
		{
			if (Pin->Direction == EGPD_Input && Pin->LinkedTo.Num() > 0)
			{
				return BuildExpression(Pin->LinkedTo[0]->GetOwningNode(), Visited);
			}
		}
		return TEXT("???");
	}

	// Variable get
	if (UK2Node_VariableGet* VarGet = Cast<UK2Node_VariableGet>(Node))
	{
		return VarGet->GetVarName().ToString();
	}

	// Call function (operators, NOT, etc.)
	if (UK2Node_CallFunction* CallFunc = Cast<UK2Node_CallFunction>(Node))
	{
		FName MemberName = CallFunc->FunctionReference.GetMemberName();
		FString FuncName = MemberName.ToString();

		// Unary NOT
		if (FuncName == TEXT("Not_PreBool"))
		{
			for (UEdGraphPin* Pin : Node->Pins)
			{
				if (Pin->Direction == EGPD_Input && !IsExecPin(Pin) && Pin->PinName != TEXT("self"))
				{
					if (Pin->LinkedTo.Num() > 0)
					{
						return FString::Printf(TEXT("NOT %s"),
							*BuildExpression(Pin->LinkedTo[0]->GetOwningNode(), Visited));
					}
				}
			}
			return TEXT("NOT ???");
		}

		// Binary operator
		FString OpSymbol = ResolveOperatorSymbol(FuncName);
		if (!OpSymbol.IsEmpty())
		{
			TArray<FString> Operands;
			for (UEdGraphPin* Pin : Node->Pins)
			{
				if (Pin->Direction == EGPD_Input && !IsExecPin(Pin)
					&& Pin->PinName != TEXT("self") && Pin->PinName != TEXT("ErrorTolerance"))
				{
					if (Pin->LinkedTo.Num() > 0)
					{
						Operands.Add(BuildExpression(Pin->LinkedTo[0]->GetOwningNode(), Visited));
					}
					else if (!Pin->DefaultValue.IsEmpty())
					{
						Operands.Add(Pin->DefaultValue);
					}
					else
					{
						// Default for numeric/bool
						FString Cat = Pin->PinType.PinCategory.ToString();
						if (Cat == TEXT("bool"))
							Operands.Add(TEXT("false"));
						else
							Operands.Add(TEXT("0"));
					}
				}
			}
			if (Operands.Num() >= 2)
			{
				return FString::Printf(TEXT("%s %s %s"), *Operands[0], *OpSymbol, *Operands[1]);
			}
			if (Operands.Num() == 1)
			{
				return FString::Printf(TEXT("%s %s 0"), *Operands[0], *OpSymbol);
			}
			return OpSymbol;
		}

		return FuncName;
	}

	// Fallback: use node title
	FText Title = Node->GetNodeTitle(ENodeTitleType::ListView);
	return Title.IsEmpty() ? Node->GetName() : Title.ToString();
}

FString FBlueprintGraphReader::BuildTransitionRuleExpression(UEdGraph* TransitionGraph)
{
	if (!TransitionGraph) return TEXT("(no rule graph)");

	// Find TransitionResult node
	UAnimGraphNode_TransitionResult* ResultNode = nullptr;
	for (UEdGraphNode* Node : TransitionGraph->Nodes)
	{
		if (UAnimGraphNode_TransitionResult* TR = Cast<UAnimGraphNode_TransitionResult>(Node))
		{
			ResultNode = TR;
			break;
		}
	}

	if (!ResultNode) return TEXT("(no transition result)");

	// Find the boolean input pin and walk backward
	for (UEdGraphPin* Pin : ResultNode->Pins)
	{
		if (Pin->Direction == EGPD_Input && !IsExecPin(Pin) && Pin->PinName != TEXT("self"))
		{
			if (Pin->LinkedTo.Num() > 0)
			{
				TSet<UEdGraphNode*> Visited;
				return BuildExpression(Pin->LinkedTo[0]->GetOwningNode(), Visited);
			}
			if (!Pin->DefaultValue.IsEmpty())
			{
				return Pin->DefaultValue;
			}
		}
	}

	return TEXT("(no rule)");
}

void FBlueprintGraphReader::FormatStateMachine(UAnimGraphNode_StateMachine* SMNode, FString& Output,
	TSharedPtr<FJsonObject>& SMMetadata)
{
	if (!SMNode) return;

	UAnimationStateMachineGraph* SMGraph = SMNode->EditorStateMachineGraph;
	if (!SMGraph) return;

	FString SMName = SMNode->GetStateMachineName();
	Output += FString::Printf(TEXT("\n=== State Machine: %s ===\n\n"), *SMName);

	SMMetadata = MakeShared<FJsonObject>();
	SMMetadata->SetStringField(TEXT("name"), SMName);

	// Collect states and entry
	TMap<UEdGraphNode*, FString> StateNames;
	UAnimStateEntryNode* EntryNode = nullptr;
	TArray<UAnimStateTransitionNode*> Transitions;

	for (UEdGraphNode* Node : SMGraph->Nodes)
	{
		if (UAnimStateNode* StateNode = Cast<UAnimStateNode>(Node))
		{
			FString StateName = StateNode->GetNodeTitle(ENodeTitleType::ListView).ToString();
			StateNames.Add(Node, StateName);
		}
		else if (UAnimStateEntryNode* Entry = Cast<UAnimStateEntryNode>(Node))
		{
			EntryNode = Entry;
		}
		else if (UAnimStateTransitionNode* Trans = Cast<UAnimStateTransitionNode>(Node))
		{
			Transitions.Add(Trans);
		}
	}

	// Entry state
	FString EntryStateName;
	if (EntryNode)
	{
		for (UEdGraphPin* Pin : EntryNode->Pins)
		{
			if (Pin->Direction == EGPD_Output && Pin->LinkedTo.Num() > 0)
			{
				UEdGraphNode* TargetNode = Pin->LinkedTo[0]->GetOwningNode();
				if (FString* Name = StateNames.Find(TargetNode))
				{
					EntryStateName = *Name;
				}
				break;
			}
		}
	}

	if (!EntryStateName.IsEmpty())
	{
		Output += FString::Printf(TEXT("Entry -> %s\n\n"), *EntryStateName);
		SMMetadata->SetStringField(TEXT("entry_state"), EntryStateName);
	}

	// Transitions
	if (Transitions.Num() > 0)
	{
		Output += TEXT("Transitions:\n");
		TArray<TSharedPtr<FJsonValue>> TransArray;

		for (UAnimStateTransitionNode* Trans : Transitions)
		{
			FString Source, Target;

			for (UEdGraphPin* Pin : Trans->Pins)
			{
				for (UEdGraphPin* LinkedPin : Pin->LinkedTo)
				{
					UEdGraphNode* LinkedNode = LinkedPin->GetOwningNode();
					if (FString* Name = StateNames.Find(LinkedNode))
					{
						if (Pin->Direction == EGPD_Input)
						{
							Source = *Name;
						}
						else
						{
							Target = *Name;
						}
					}
				}
			}

			FString Rule;
			if (Trans->bAutomaticRuleBasedOnSequencePlayerInState)
			{
				Rule = TEXT("[automatic - sequence player finished]");
			}
			else
			{
				UEdGraph* TransGraph = Trans->GetBoundGraph();
				Rule = BuildTransitionRuleExpression(TransGraph);
			}

			Output += FString::Printf(TEXT("  %s -> %s:  %s\n"),
				Source.IsEmpty() ? TEXT("???") : *Source,
				Target.IsEmpty() ? TEXT("???") : *Target,
				*Rule);

			TSharedPtr<FJsonObject> TransJson = MakeShared<FJsonObject>();
			TransJson->SetStringField(TEXT("source"), Source.IsEmpty() ? TEXT("???") : Source);
			TransJson->SetStringField(TEXT("target"), Target.IsEmpty() ? TEXT("???") : Target);
			TransJson->SetStringField(TEXT("rule"), Rule);
			TransArray.Add(MakeShared<FJsonValueObject>(TransJson));
		}

		SMMetadata->SetNumberField(TEXT("transition_count"), Transitions.Num());
		SMMetadata->SetArrayField(TEXT("transitions"), TransArray);
		Output += TEXT("\n");
	}

	// States with their animations
	if (StateNames.Num() > 0)
	{
		Output += TEXT("States:\n");
		TArray<TSharedPtr<FJsonValue>> StateArray;

		// Sort states by name
		TArray<TPair<UEdGraphNode*, FString>> SortedStates;
		for (auto& Pair : StateNames)
		{
			SortedStates.Add(TPair<UEdGraphNode*, FString>(Pair.Key, Pair.Value));
		}
		SortedStates.Sort([](const auto& A, const auto& B) { return A.Value < B.Value; });

		for (auto& Pair : SortedStates)
		{
			UAnimStateNode* StateNode = Cast<UAnimStateNode>(Pair.Key);
			FString StateName = Pair.Value;

			Output += FString::Printf(TEXT("  %s:\n"), *StateName);
			StateArray.Add(MakeShared<FJsonValueString>(StateName));

			if (StateNode)
			{
				UEdGraph* StateGraph = StateNode->GetBoundGraph();
				if (StateGraph)
				{
					// Find StateResult node and walk pose chain
					for (UEdGraphNode* Node : StateGraph->Nodes)
					{
						if (Cast<UAnimGraphNode_StateResult>(Node))
						{
							TSet<UEdGraphNode*> Visited;
							FString PoseOutput;
							WalkPoseChain(Node, 0, PoseOutput, Visited);

							// Indent state animation
							TArray<FString> Lines;
							PoseOutput.ParseIntoArrayLines(Lines);
							for (const FString& Line : Lines)
							{
								if (!Line.IsEmpty())
								{
									Output += FString::Printf(TEXT("    %s\n"), *Line);
								}
							}
							break;
						}
					}
				}
				else
				{
					Output += TEXT("    (no state graph)\n");
				}
			}
		}

		SMMetadata->SetNumberField(TEXT("state_count"), StateNames.Num());
		SMMetadata->SetArrayField(TEXT("states"), StateArray);
		Output += TEXT("\n");
	}
}

