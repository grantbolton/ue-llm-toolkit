static TMap<FString, FString> InitOperatorMap()
{
	TMap<FString, FString> Map;
	Map.Add(TEXT("Greater"), TEXT(">"));
	Map.Add(TEXT("Less"), TEXT("<"));
	Map.Add(TEXT("Equal"), TEXT("=="));
	Map.Add(TEXT("EqualEqual"), TEXT("=="));
	Map.Add(TEXT("GreaterEqual"), TEXT(">="));
	Map.Add(TEXT("LessEqual"), TEXT("<="));
	Map.Add(TEXT("NotEqual"), TEXT("!="));
	Map.Add(TEXT("Add"), TEXT("+"));
	Map.Add(TEXT("Subtract"), TEXT("-"));
	Map.Add(TEXT("Multiply"), TEXT("*"));
	Map.Add(TEXT("Divide"), TEXT("/"));
	Map.Add(TEXT("BooleanAND"), TEXT("AND"));
	Map.Add(TEXT("BooleanOR"), TEXT("OR"));
	return Map;
}

static const TMap<FString, FString>& GetOperatorMap()
{
	static TMap<FString, FString> Map = InitOperatorMap();
	return Map;
}

