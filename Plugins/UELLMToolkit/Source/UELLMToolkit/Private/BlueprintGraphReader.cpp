void FBlueprintGraphReader::DeriveK2NodeTypeAndContext(UEdGraphNode* Node, FString& OutNodeType, FString& OutContext)
{
	OutNodeType = TEXT("Node");
	OutContext = FString();

	if (UK2Node_Event* Event = Cast<UK2Node_Event>(Node))
	{
		OutNodeType = TEXT("Event");
		OutContext = Event->EventReference.GetMemberName().ToString();
	}
	else if (UK2Node_CustomEvent* Custom = Cast<UK2Node_CustomEvent>(Node))
	{
		OutNodeType = TEXT("CustomEvent");
		OutContext = Custom->CustomFunctionName.ToString();
	}
	else if (UK2Node_FunctionEntry* FuncEntry = Cast<UK2Node_FunctionEntry>(Node))
	{
		OutNodeType = TEXT("FunctionEntry");
		FString FuncName = FuncEntry->FunctionReference.GetMemberName().ToString();
		if (FuncName.IsEmpty()) FuncName = FuncEntry->CustomGeneratedFunctionName.ToString();
		OutContext = FuncName;
	}
	else if (Cast<UK2Node_FunctionResult>(Node))
	{
		OutNodeType = TEXT("FunctionResult");
	}
	else if (UK2Node_CallFunction* Call = Cast<UK2Node_CallFunction>(Node))
	{
		OutNodeType = TEXT("CallFunction");
		OutContext = Call->FunctionReference.GetMemberName().ToString();
	}
	else if (UK2Node_VariableGet* VarGet = Cast<UK2Node_VariableGet>(Node))
	{
		OutNodeType = TEXT("VarGet");
		OutContext = VarGet->GetVarName().ToString();
	}
	else if (UK2Node_VariableSet* VarSet = Cast<UK2Node_VariableSet>(Node))
	{
		OutNodeType = TEXT("VarSet");
		OutContext = VarSet->GetVarName().ToString();
	}
	else if (Cast<UK2Node_IfThenElse>(Node))
	{
		OutNodeType = TEXT("Branch");
	}
	else if (UK2Node_DynamicCast* DynCast = Cast<UK2Node_DynamicCast>(Node))
	{
		OutNodeType = TEXT("Cast");
		if (DynCast->TargetType)
		{
			OutContext = DynCast->TargetType->GetName();
		}
	}
	else if (UK2Node_MacroInstance* Macro = Cast<UK2Node_MacroInstance>(Node))
	{
		OutNodeType = TEXT("Macro");
		UEdGraph* MacroGraph = Macro->GetMacroGraph();
		if (MacroGraph) OutContext = MacroGraph->GetName();
	}
	else if (Cast<UK2Node_SwitchEnum>(Node))
	{
		OutNodeType = TEXT("SwitchEnum");
	}
	else if (Cast<UK2Node_SpawnActorFromClass>(Node))
	{
		OutNodeType = TEXT("SpawnActor");
	}
	else if (Cast<UK2Node_AssignmentStatement>(Node))
	{
		OutNodeType = TEXT("Assign");
	}
	else if (Cast<UK2Node_MakeArray>(Node))
	{
		OutNodeType = TEXT("MakeArray");
	}
	else if (Cast<UK2Node_Select>(Node))
	{
		OutNodeType = TEXT("Select");
	}
	else if (Cast<UK2Node_Self>(Node))
	{
		OutNodeType = TEXT("Self");
	}

	OutContext.ReplaceInline(TEXT(" "), TEXT("_"));
}

TSharedPtr<FJsonObject> FBlueprintGraphReader::SerializeK2NodeInfo(UEdGraphNode* Node, UEdGraph* Graph)
{
	TSharedPtr<FJsonObject> Json = MakeShared<FJsonObject>();
	if (!Node) return Json;

	FString NodeId = FBlueprintGraphEditor::GetNodeId(Node);
	if (NodeId.IsEmpty())
	{
		FString NodeType, Context;
		DeriveK2NodeTypeAndContext(Node, NodeType, Context);
		NodeId = FBlueprintGraphEditor::GenerateNodeId(NodeType, Context, Graph);
		FBlueprintGraphEditor::SetNodeId(Node, NodeId);
	}

	Json->SetStringField(TEXT("node_id"), NodeId);
	Json->SetStringField(TEXT("node_class"), Node->GetClass()->GetName());
	Json->SetStringField(TEXT("label"), GetK2NodeLabel(Node));
	Json->SetNumberField(TEXT("pos_x"), Node->NodePosX);
	Json->SetNumberField(TEXT("pos_y"), Node->NodePosY);

	TArray<TSharedPtr<FJsonValue>> PinsArray;
	for (UEdGraphPin* Pin : Node->Pins)
	{
		if (!Pin || Pin->bHidden) continue;

		TSharedPtr<FJsonObject> PinJson = MakeShared<FJsonObject>();
		PinJson->SetStringField(TEXT("name"), Pin->PinName.ToString());
		PinJson->SetStringField(TEXT("direction"), Pin->Direction == EGPD_Input ? TEXT("Input") : TEXT("Output"));
		PinJson->SetStringField(TEXT("type"), Pin->PinType.PinCategory.ToString());
		PinJson->SetBoolField(TEXT("connected"), Pin->LinkedTo.Num() > 0);

		if (Pin->LinkedTo.Num() > 0)
		{
			TArray<TSharedPtr<FJsonValue>> LinkedArray;
			for (UEdGraphPin* Linked : Pin->LinkedTo)
			{
				if (!Linked) continue;
				UEdGraphNode* LinkedNode = Linked->GetOwningNode();
				if (!LinkedNode) continue;

				FString LinkedId = FBlueprintGraphEditor::GetNodeId(LinkedNode);
				TSharedPtr<FJsonObject> LinkJson = MakeShared<FJsonObject>();
				LinkJson->SetStringField(TEXT("node_id"), LinkedId);
				LinkJson->SetStringField(TEXT("pin_name"), Linked->PinName.ToString());
				LinkedArray.Add(MakeShared<FJsonValueObject>(LinkJson));
			}
			PinJson->SetArrayField(TEXT("linked_to"), LinkedArray);
		}

		if (!Pin->DefaultValue.IsEmpty())
		{
			PinJson->SetStringField(TEXT("default_value"), Pin->DefaultValue);
		}

		PinsArray.Add(MakeShared<FJsonValueObject>(PinJson));
	}
	Json->SetArrayField(TEXT("pins"), PinsArray);

	return Json;
}

