FString FBlueprintGraphReader::GetNodeLabel(UEdGraphNode* Node)
{
	if (!Node) return FString();

	// Check for knot (reroute) nodes — return empty to skip
	if (Cast<UK2Node_Knot>(Node))
	{
		return FString();
	}

	// Try K2 node label first
	if (Cast<UK2Node>(Node))
	{
		return GetK2NodeLabel(Node);
	}

	// Try anim graph node label
	if (Cast<UAnimGraphNode_Base>(Node) || Cast<UAnimStateNodeBase>(Node) || Cast<UAnimStateEntryNode>(Node))
	{
		return GetAnimNodeLabel(Node);
	}

	// Fallback: use UE title
	return Node->GetNodeTitle(ENodeTitleType::ListView).ToString();
}

FString FBlueprintGraphReader::GetK2NodeLabel(UEdGraphNode* Node)
{
	// Event
	if (UK2Node_Event* Event = Cast<UK2Node_Event>(Node))
	{
		FName EventName = Event->EventReference.GetMemberName();
		return FString::Printf(TEXT("Event: %s"), *EventName.ToString());
	}

	// Custom Event
	if (UK2Node_CustomEvent* Custom = Cast<UK2Node_CustomEvent>(Node))
	{
		FName FuncName = Custom->CustomFunctionName;
		return FString::Printf(TEXT("CustomEvent: %s"), *FuncName.ToString());
	}

	// Function Entry
	if (UK2Node_FunctionEntry* FuncEntry = Cast<UK2Node_FunctionEntry>(Node))
	{
		FString FuncName = FuncEntry->FunctionReference.GetMemberName().ToString();
		if (FuncName.IsEmpty())
		{
			FuncName = FuncEntry->CustomGeneratedFunctionName.ToString();
		}
		return FString::Printf(TEXT("FunctionEntry: %s"), *FuncName);
	}

	// Function Result
	if (Cast<UK2Node_FunctionResult>(Node))
	{
		return TEXT("FunctionResult");
	}

	// Call Function
	if (UK2Node_CallFunction* Call = Cast<UK2Node_CallFunction>(Node))
	{
		FName FuncName = Call->FunctionReference.GetMemberName();
		UClass* MemberParent = Call->FunctionReference.GetMemberParentClass();
		if (MemberParent)
		{
			return FString::Printf(TEXT("Call %s::%s()"), *MemberParent->GetName(), *FuncName.ToString());
		}
		return FString::Printf(TEXT("Call %s()"), *FuncName.ToString());
	}

	// Variable Get/Set
	if (UK2Node_VariableGet* VarGet = Cast<UK2Node_VariableGet>(Node))
	{
		return FString::Printf(TEXT("Get %s"), *VarGet->GetVarName().ToString());
	}
	if (UK2Node_VariableSet* VarSet = Cast<UK2Node_VariableSet>(Node))
	{
		return FString::Printf(TEXT("Set %s"), *VarSet->GetVarName().ToString());
	}

	// Branch
	if (Cast<UK2Node_IfThenElse>(Node))
	{
		return TEXT("Branch");
	}

	// Cast
	if (UK2Node_DynamicCast* DynCast = Cast<UK2Node_DynamicCast>(Node))
	{
		UClass* TargetClass = DynCast->TargetType;
		return FString::Printf(TEXT("Cast to %s"), TargetClass ? *TargetClass->GetName() : TEXT("???"));
	}

	// Macro
	if (UK2Node_MacroInstance* Macro = Cast<UK2Node_MacroInstance>(Node))
	{
		UEdGraph* MacroGraph = Macro->GetMacroGraph();
		FString MacroName = MacroGraph ? MacroGraph->GetName() : TEXT("Unknown");
		return FString::Printf(TEXT("Macro: %s"), *MacroName);
	}

	// Switch
	if (Cast<UK2Node_SwitchEnum>(Node))
	{
		return TEXT("Switch on Enum");
	}

	// Self
	if (Cast<UK2Node_Self>(Node))
	{
		return TEXT("Self");
	}

	// Select
	if (Cast<UK2Node_Select>(Node))
	{
		return TEXT("Select");
	}

	// Make Array
	if (Cast<UK2Node_MakeArray>(Node))
	{
		return TEXT("Make Array");
	}

	// Spawn Actor
	if (Cast<UK2Node_SpawnActorFromClass>(Node))
	{
		return TEXT("Spawn Actor");
	}

	// Assignment
	if (Cast<UK2Node_AssignmentStatement>(Node))
	{
		return TEXT("Assign");
	}

	// Temp Variable
	if (Cast<UK2Node_TemporaryVariable>(Node))
	{
		return TEXT("TempVar");
	}

	// Fallback: use node title
	FText Title = Node->GetNodeTitle(ENodeTitleType::ListView);
	if (!Title.IsEmpty())
	{
		return Title.ToString();
	}

	return Node->GetClass()->GetName();
}

FString FBlueprintGraphReader::GetAnimNodeLabel(UEdGraphNode* Node)
{
	// Root output pose
	if (Cast<UAnimGraphNode_Root>(Node))
	{
		return TEXT("Output Pose");
	}

	// State result (output pose in state)
	if (Cast<UAnimGraphNode_StateResult>(Node))
	{
		return TEXT("Output Pose");
	}

	// Transition result
	if (Cast<UAnimGraphNode_TransitionResult>(Node))
	{
		return TEXT("Transition Result");
	}

	// State machine
	if (UAnimGraphNode_StateMachine* SM = Cast<UAnimGraphNode_StateMachine>(Node))
	{
		FString SMName = SM->GetNodeTitle(ENodeTitleType::ListView).ToString();
		if (SMName.IsEmpty())
		{
			SMName = SM->GetStateMachineName();
		}
		return FString::Printf(TEXT("State Machine: %s"), *SMName);
	}

	// Slot
	if (UAnimGraphNode_Slot* Slot = Cast<UAnimGraphNode_Slot>(Node))
	{
		FString SlotName = Slot->Node.SlotName.ToString();
		return FString::Printf(TEXT("Slot (%s)"), *SlotName);
	}

	// Save cached pose
	if (UAnimGraphNode_SaveCachedPose* SavePose = Cast<UAnimGraphNode_SaveCachedPose>(Node))
	{
		return FString::Printf(TEXT("Save Cached Pose: %s"), *SavePose->CacheName);
	}

	// Use cached pose
	if (UAnimGraphNode_UseCachedPose* UsePose = Cast<UAnimGraphNode_UseCachedPose>(Node))
	{
		FString LinkName = UsePose->GetNodeTitle(ENodeTitleType::ListView).ToString();
		return FString::Printf(TEXT("Use Cached Pose: %s"), *LinkName);
	}

	// State node (in state machine)
	if (UAnimStateNode* StateNode = Cast<UAnimStateNode>(Node))
	{
		return FString::Printf(TEXT("State: %s"),
			*StateNode->GetNodeTitle(ENodeTitleType::ListView).ToString());
	}

	// Entry node
	if (Cast<UAnimStateEntryNode>(Node))
	{
		return TEXT("Entry");
	}

	// Transition node
	if (UAnimStateTransitionNode* Trans = Cast<UAnimStateTransitionNode>(Node))
	{
		if (Trans->bAutomaticRuleBasedOnSequencePlayerInState)
		{
			return TEXT("Transition [automatic]");
		}
		return TEXT("Transition");
	}

	// Linked Anim Layer
	if (UAnimGraphNode_LinkedAnimLayer* LayerNode = Cast<UAnimGraphNode_LinkedAnimLayer>(Node))
	{
		FName LayerName = LayerNode->Node.Layer;
		UClass* InterfaceClass = *LayerNode->Node.Interface;
		if (InterfaceClass)
		{
			return FString::Printf(TEXT("Linked Anim Layer: %s (Interface: %s)"),
				*LayerName.ToString(), *InterfaceClass->GetName());
		}
		return FString::Printf(TEXT("Anim Layer (self): %s"), *LayerName.ToString());
	}

	// For all other anim graph nodes, use the node title (which is typically good)
	if (UAnimGraphNode_Base* AnimNode = Cast<UAnimGraphNode_Base>(Node))
	{
		FText Title = AnimNode->GetNodeTitle(ENodeTitleType::ListView);
		if (!Title.IsEmpty())
		{
			return Title.ToString();
		}
	}

	return Node->GetNodeTitle(ENodeTitleType::ListView).ToString();
}

