FBlueprintGraphReader::EGraphType FBlueprintGraphReader::ClassifyGraph(UEdGraph* Graph)
{
	if (!Graph) return EGraphType::Other;

	for (UEdGraphNode* Node : Graph->Nodes)
	{
		if (Cast<UAnimGraphNode_Root>(Node))
		{
			return EGraphType::AnimGraph;
		}
		if (Cast<UAnimStateEntryNode>(Node))
		{
			return EGraphType::StateMachine;
		}
	}

	for (UEdGraphNode* Node : Graph->Nodes)
	{
		if (Cast<UAnimGraphNode_Base>(Node))
		{
			return EGraphType::AnimGraph;
		}
	}

	for (UEdGraphNode* Node : Graph->Nodes)
	{
		if (Cast<UK2Node_Event>(Node) || Cast<UK2Node_CustomEvent>(Node) || Cast<UK2Node_FunctionEntry>(Node))
		{
			return EGraphType::EventGraph;
		}
	}

	return EGraphType::Other;
}

