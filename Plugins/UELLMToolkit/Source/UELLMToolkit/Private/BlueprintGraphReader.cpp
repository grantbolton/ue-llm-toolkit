bool FBlueprintGraphReader::IsExecPin(UEdGraphPin* Pin)
{
	return Pin && Pin->PinType.PinCategory == UEdGraphSchema_K2::PC_Exec;
}

bool FBlueprintGraphReader::IsPoseLinkPin(UEdGraphPin* Pin)
{
	if (!Pin) return false;
	UObject* SubObj = Pin->PinType.PinSubCategoryObject.Get();
	UScriptStruct* Struct = Cast<UScriptStruct>(SubObj);
	if (!Struct) return false;
	static UScriptStruct* PoseLinkBase = FPoseLinkBase::StaticStruct();
	return Struct->IsChildOf(PoseLinkBase);
}

TArray<UEdGraphPin*> FBlueprintGraphReader::GetExecOutputPins(UEdGraphNode* Node)
{
	TArray<UEdGraphPin*> Result;
	if (!Node) return Result;
	for (UEdGraphPin* Pin : Node->Pins)
	{
		if (Pin->Direction == EGPD_Output && IsExecPin(Pin) && !Pin->bHidden)
		{
			Result.Add(Pin);
		}
	}
	return Result;
}

TArray<UEdGraphPin*> FBlueprintGraphReader::GetPoseInputPins(UEdGraphNode* Node)
{
	TArray<UEdGraphPin*> Result;
	if (!Node) return Result;
	for (UEdGraphPin* Pin : Node->Pins)
	{
		if (Pin->Direction == EGPD_Input && IsPoseLinkPin(Pin))
		{
			Result.Add(Pin);
		}
	}
	return Result;
}

TArray<UEdGraphPin*> FBlueprintGraphReader::GetDataInputPins(UEdGraphNode* Node)
{
	TArray<UEdGraphPin*> Result;
	if (!Node) return Result;
	for (UEdGraphPin* Pin : Node->Pins)
	{
		if (Pin->Direction == EGPD_Input
			&& !IsExecPin(Pin)
			&& !IsPoseLinkPin(Pin)
			&& !Pin->bHidden
			&& Pin->PinName != TEXT("self"))
		{
			// Only include if connected or has non-default value
			if (Pin->LinkedTo.Num() > 0)
			{
				Result.Add(Pin);
			}
			else if (!Pin->DefaultValue.IsEmpty() && Pin->DefaultValue != Pin->AutogeneratedDefaultValue)
			{
				Result.Add(Pin);
			}
		}
	}
	return Result;
}

UEdGraphNode* FBlueprintGraphReader::FollowThroughKnots(UEdGraphNode* Node, EEdGraphPinDirection Direction)
{
	if (!Node) return nullptr;

	UK2Node_Knot* Knot = Cast<UK2Node_Knot>(Node);
	int32 Safety = 100;
	while (Knot && Safety-- > 0)
	{
		UEdGraphNode* Next = nullptr;
		for (UEdGraphPin* Pin : Knot->Pins)
		{
			if (Pin->Direction == Direction && Pin->LinkedTo.Num() > 0)
			{
				Next = Pin->LinkedTo[0]->GetOwningNode();
				break;
			}
		}
		if (!Next) break;
		Knot = Cast<UK2Node_Knot>(Next);
		if (!Knot) return Next;
	}
	return Node;
}

