FBlueprintGraphReader::FGraphReadResult FBlueprintGraphReader::ReadEventGraph(
	UBlueprint* Blueprint, const FString& GraphName)
{
	FGraphReadResult Result;
	Result.Metadata = MakeShared<FJsonObject>();

	if (!Blueprint)
	{
		Result.Summary = TEXT("(null blueprint)");
		return Result;
	}

	Result.Metadata->SetStringField(TEXT("blueprint_name"), Blueprint->GetName());

	TArray<UEdGraph*> AllGraphs;
	Blueprint->GetAllGraphs(AllGraphs);

	FString Output;
	Output += FString::Printf(TEXT("============================================================\n"));
	Output += FString::Printf(TEXT("Blueprint Event Graphs: %s\n"), *Blueprint->GetName());
	Output += FString::Printf(TEXT("============================================================\n"));

	TArray<TSharedPtr<FJsonValue>> GraphInfoArray;
	int32 ProcessedGraphs = 0;

	for (UEdGraph* Graph : AllGraphs)
	{
		if (!Graph) continue;

		// Filter by name if specified
		if (!GraphName.IsEmpty() && Graph->GetName() != GraphName)
		{
			continue;
		}

		EGraphType GType = ClassifyGraph(Graph);
		if (GType != EGraphType::EventGraph) continue;

		// Find entry nodes
		TArray<UEdGraphNode*> EntryNodes;
		for (UEdGraphNode* Node : Graph->Nodes)
		{
			if (Cast<UK2Node_Event>(Node) || Cast<UK2Node_CustomEvent>(Node) || Cast<UK2Node_FunctionEntry>(Node))
			{
				EntryNodes.Add(Node);
			}
		}

		if (EntryNodes.Num() == 0)
		{
			// Check if there are any non-intermediate K2 nodes
			bool bHasContent = false;
			for (UEdGraphNode* Node : Graph->Nodes)
			{
				if (Cast<UK2Node>(Node) && !Cast<UK2Node_Knot>(Node))
				{
					bHasContent = true;
					break;
				}
			}
			if (!bHasContent) continue;
		}

		Output += FString::Printf(TEXT("\n--- %s ---\n\n"), *Graph->GetName());
		ProcessedGraphs++;

		TSharedPtr<FJsonObject> GraphMeta = MakeShared<FJsonObject>();
		GraphMeta->SetStringField(TEXT("name"), Graph->GetName());
		GraphMeta->SetStringField(TEXT("type"), TEXT("EventGraph"));
		GraphMeta->SetNumberField(TEXT("node_count"), Graph->Nodes.Num());

		TArray<TSharedPtr<FJsonValue>> NodesArray;
		for (UEdGraphNode* Node : Graph->Nodes)
		{
			if (!Node) continue;
			if (Cast<UK2Node_Knot>(Node)) continue;
			if (Node->GetClass()->GetName().Contains(TEXT("Comment"))) continue;

			NodesArray.Add(MakeShared<FJsonValueObject>(SerializeK2NodeInfo(Node, Graph)));
		}
		GraphMeta->SetArrayField(TEXT("nodes"), NodesArray);
		GraphInfoArray.Add(MakeShared<FJsonValueObject>(GraphMeta));

		if (EntryNodes.Num() == 0)
		{
			Output += TEXT("  (no event/entry nodes found)\n\n");
			continue;
		}

		for (UEdGraphNode* EntryNode : EntryNodes)
		{
			TSet<UEdGraphNode*> Visited;
			WalkExecChain(EntryNode, 0, Output, Visited);
			Output += TEXT("\n");
		}
	}

	if (ProcessedGraphs == 0)
	{
		Output += TEXT("\n(no event graphs found)\n");
	}

	Result.Summary = Output;
	Result.Metadata->SetNumberField(TEXT("graph_count"), ProcessedGraphs);
	Result.Metadata->SetArrayField(TEXT("graphs"), GraphInfoArray);

	return Result;
}

FBlueprintGraphReader::FGraphReadResult FBlueprintGraphReader::ReadAnimGraph(
	UBlueprint* Blueprint, const FString& GraphName)
{
	FGraphReadResult Result;
	Result.Metadata = MakeShared<FJsonObject>();

	if (!Blueprint)
	{
		Result.Summary = TEXT("(null blueprint)");
		return Result;
	}

	Result.Metadata->SetStringField(TEXT("blueprint_name"), Blueprint->GetName());

	TArray<UEdGraph*> AllGraphs;
	Blueprint->GetAllGraphs(AllGraphs);

	FString Output;
	Output += FString::Printf(TEXT("============================================================\n"));
	Output += FString::Printf(TEXT("AnimGraph: %s\n"), *Blueprint->GetName());
	Output += FString::Printf(TEXT("============================================================\n"));

	TArray<TSharedPtr<FJsonValue>> GraphInfoArray;
	int32 ProcessedGraphs = 0;

	for (UEdGraph* Graph : AllGraphs)
	{
		if (!Graph) continue;

		if (!GraphName.IsEmpty() && Graph->GetName() != GraphName)
		{
			continue;
		}

		EGraphType GType = ClassifyGraph(Graph);
		if (GType != EGraphType::AnimGraph) continue;

		// Find Root node
		UAnimGraphNode_Root* RootNode = nullptr;
		for (UEdGraphNode* Node : Graph->Nodes)
		{
			if (UAnimGraphNode_Root* Root = Cast<UAnimGraphNode_Root>(Node))
			{
				RootNode = Root;
				break;
			}
		}

		if (!RootNode) continue;

		Output += FString::Printf(TEXT("\n--- %s (AnimGraph) ---\n\n"), *Graph->GetName());
		ProcessedGraphs++;

		TSharedPtr<FJsonObject> GraphMeta = MakeShared<FJsonObject>();
		GraphMeta->SetStringField(TEXT("name"), Graph->GetName());
		GraphMeta->SetStringField(TEXT("type"), TEXT("AnimGraph"));
		GraphMeta->SetNumberField(TEXT("node_count"), Graph->Nodes.Num());
		GraphInfoArray.Add(MakeShared<FJsonValueObject>(GraphMeta));

		TSet<UEdGraphNode*> Visited;
		WalkPoseChain(RootNode, 0, Output, Visited);

		// Also note K2 nodes in the anim graph (event handlers etc.)
		TArray<UEdGraphNode*> K2Nodes;
		for (UEdGraphNode* Node : Graph->Nodes)
		{
			if (Cast<UK2Node>(Node) && !Cast<UK2Node_Knot>(Node))
			{
				K2Nodes.Add(Node);
			}
		}
		if (K2Nodes.Num() > 0)
		{
			Output += FString::Printf(TEXT("\n  K2 nodes in %s:\n"), *Graph->GetName());
			for (UEdGraphNode* K2Node : K2Nodes)
			{
				FString Label = GetNodeLabel(K2Node);
				if (!Label.IsEmpty())
				{
					Output += FString::Printf(TEXT("    %s\n"), *Label);
				}
			}
		}

		Output += TEXT("\n");
	}

	if (ProcessedGraphs == 0)
	{
		Output += TEXT("\n(no anim graphs found)\n");
	}

	Result.Summary = Output;
	Result.Metadata->SetNumberField(TEXT("graph_count"), ProcessedGraphs);
	Result.Metadata->SetArrayField(TEXT("graphs"), GraphInfoArray);

	return Result;
}

FBlueprintGraphReader::FGraphReadResult FBlueprintGraphReader::ReadStateMachineDetail(
	UBlueprint* Blueprint, const FString& StateMachineName)
{
	FGraphReadResult Result;
	Result.Metadata = MakeShared<FJsonObject>();

	if (!Blueprint)
	{
		Result.Summary = TEXT("(null blueprint)");
		return Result;
	}

	Result.Metadata->SetStringField(TEXT("blueprint_name"), Blueprint->GetName());

	TArray<UEdGraph*> AllGraphs;
	Blueprint->GetAllGraphs(AllGraphs);

	FString Output;
	Output += FString::Printf(TEXT("============================================================\n"));
	Output += FString::Printf(TEXT("State Machine Detail: %s\n"), *Blueprint->GetName());
	Output += FString::Printf(TEXT("============================================================\n"));

	TArray<TSharedPtr<FJsonValue>> SMInfoArray;
	int32 ProcessedSMs = 0;

	// Find state machine nodes in all anim graphs
	for (UEdGraph* Graph : AllGraphs)
	{
		if (!Graph) continue;

		for (UEdGraphNode* Node : Graph->Nodes)
		{
			UAnimGraphNode_StateMachine* SMNode = Cast<UAnimGraphNode_StateMachine>(Node);
			if (!SMNode) continue;

			FString SMName = SMNode->GetStateMachineName();

			// Filter by name if specified
			if (!StateMachineName.IsEmpty() && SMName != StateMachineName)
			{
				continue;
			}

			ProcessedSMs++;
			TSharedPtr<FJsonObject> SMMetadata;
			FormatStateMachine(SMNode, Output, SMMetadata);

			if (SMMetadata.IsValid())
			{
				SMInfoArray.Add(MakeShared<FJsonValueObject>(SMMetadata));
			}
		}
	}

	if (ProcessedSMs == 0)
	{
		Output += TEXT("\n(no state machines found)\n");
	}

	Result.Summary = Output;
	Result.Metadata->SetNumberField(TEXT("state_machine_count"), ProcessedSMs);
	Result.Metadata->SetArrayField(TEXT("state_machines"), SMInfoArray);

	return Result;
}

